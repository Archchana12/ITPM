    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        String semi= txt_year.getText();
        String prog = cmb_pro.getSelectedItem().toString();
        String gno = cmb_gno.getSelectedItem().toString();
        String sno = cmb_sno.getSelectedItem().toString();
        String groupid= txt_gid.getText();
        String subgroupid= txt_sid.getText();
        try {
            pst=con.prepareStatement("insert into student_group (semi,prog,gno,sno,groupid,subgroupid) values(?,?,?,?,?,?)");
           
            pst.setString(1,semi );
            pst.setString(2, prog);
            pst.setString(3, gno);
            pst.setString(4, sno);
            pst.setString(5, groupid);
            pst.setString(6, subgroupid);
           int k = pst.executeUpdate();
           
           if(k==1){
               JOptionPane.showMessageDialog(this," Added");
             
                txt_year.setText("");
                cmb_pro.setSelectedItem("");
                cmb_gno.setSelectedItem("");
                cmb_sno.setSelectedItem("");
                txt_gid.setText("");
                txt_sid.setText("");
                txt_year.requestFocus();
              
                Load();
               
               
           }
           else{
               JOptionPane.showMessageDialog(this,"Error");
           }
            
        } catch (SQLException ex) {
            Logger.getLogger(add_student.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_btn_saveActionPerformed

