    public void Load(){
        int c;
        try {
            pst=con.prepareStatement("select* from student_group");
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd=rs.getMetaData();
            c=rsd.getColumnCount();
            
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next()){
                Vector v2= new Vector();
                for(int i=1;i<=c;i++){
                   
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("semi"));
                    v2.add(rs.getString("prog"));
                    v2.add(rs.getString("gno"));
                    v2.add(rs.getString("sno"));
                    v2.add(rs.getString("groupid"));
                    v2.add(rs.getString("subgroupid"));
                    
                }
                d.addRow(v2);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(add_student.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }
    
