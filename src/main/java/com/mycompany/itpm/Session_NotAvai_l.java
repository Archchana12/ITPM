    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String RoomName= cmb_room.getSelectedItem().toString();
        String Day= cmb_day.getSelectedItem().toString();
        String StartTime= cmb_Stime.getSelectedItem().toString();
        String EndTime= cmb_Etime.getSelectedItem().toString();
       
        
        
        try {
            pst=con.prepareStatement("insert into notavailablelocation(roomName,day,startTime,endTime)values(?,?,?,?)");
           
            pst.setString(1, RoomName);
            pst.setString(2, Day);
            pst.setString(3, StartTime);
            pst.setString(4, EndTime);
          
           int k = pst.executeUpdate();
           
           if(k==1){
               JOptionPane.showMessageDialog(this," Added");
             
                
                cmb_room.setSelectedItem("");
                cmb_day.setSelectedItem("");
                cmb_Stime.setSelectedItem("");
                cmb_Etime.setSelectedItem("");
                
              
             
            ck.AddRowToJTable(new Object[]{
                
                cmb_room.getSelectedItem().toString(),cmb_day.getSelectedItem().toString(),cmb_Stime.getSelectedItem().toString(),
                    cmb_Etime.getSelectedItem().toString()
                
                }); 
               
               
           }
           else{
               JOptionPane.showMessageDialog(this,"Error");
           }
            
        } catch (SQLException ex) {
            Logger.getLogger(ck.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

