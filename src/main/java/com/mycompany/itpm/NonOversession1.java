    void  con_load(){
        int c;
        try {
            pst=con.prepareStatement("select* from sessiondetails");
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd=rs.getMetaData();
            c=rsd.getColumnCount();
            
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next()){
                Vector v2= new Vector();
                for(int i=1;i<=c;i++){
                   
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("lecturer1"));
                    v2.add(rs.getString("lecturer2"));
                    v2.add(rs.getString("subject"));
                    v2.add(rs.getString("group"));
                    v2.add(rs.getString("tag"));
                    v2.add(rs.getString("noofstudents"));
                    v2.add(rs.getString("duration"));
                    v2.add(rs.getString("room"));
                    
                    
                }
                d.addRow(v2);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(NonOversession1.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }

