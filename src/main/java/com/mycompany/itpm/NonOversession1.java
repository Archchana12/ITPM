    private void btn_btnAddSesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_btnAddSesActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1=(DefaultTableModel)jTable1.getModel();
        if(d1.getRowCount()==0){
        //if table empty
            JOptionPane.showMessageDialog(this,"This table empty");
        }
        else
        {
            //not empty
            //db connection
            
                String Sid ,Lec1 ,Lec2 ,Sub ,Group ,Tag,NoS ,Du ,Room ;
                try {
                    Class.forName("com.mysql.jdbc.Driver");
                    con=DriverManager.getConnection("jdbc:mysql://localhost:3306/timetable_manage?useSSL=false","root","");
                        for(int i=0;i<d1.getRowCount();i++){
                             Sid=d1.getValueAt(i,0).toString();
                             Lec1=d1.getValueAt(i,1).toString();
                             Lec2=d1.getValueAt(i,2).toString();
                             Sub=d1.getValueAt(i,3).toString();
                             Group=d1.getValueAt(i,4).toString();
                             Tag=d1.getValueAt(i,5).toString();
                             NoS=d1.getValueAt(i,6).toString();
                             Du=d1.getValueAt(i,7).toString();
                             Room=d1.getValueAt(i,8).toString();
                        
                     
                        
                        String query =" insert into NonOversession(ses_id,lecture1,lecture2,subject,groupid,tag,noofstudent,duration,room)values(?,?,?,?,?,?,?,?,?)";
                        pst= con.prepareStatement(query);
                        pst.setString(1, Sid);
                        pst.setString(2, Lec1);
                         pst.setString(3, Lec2);
                        pst.setString(4, Sub);
                        pst.setString(5, Group);
                        pst.setString(6, Tag);
                        pst.setString(7, NoS);
                         pst.setString(8, Du);
                          pst.setString(9, Room);
                          
                          
                        pst.execute();
                        
                    }
                     JOptionPane.showMessageDialog(this,"Consecutive sessions inserted");
                     d1.setRowCount(0);
                    
                }  catch (Exception e) {
                   
                }
                }
    }//GEN-LAST:event_btn_btnAddSesActionPerformed

