    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
    
        String noDays= cmb_nd.getSelectedItem().toString();
        String day= "";
            if(cb_mon.isSelected()){
                day+=cb_mon.getText()+"";
            }
            if(cb_tue.isSelected()){
                day+=cb_tue.getText()+"";
            }
            if(cb_wed.isSelected()){
                day+=cb_wed.getText()+"";
            }
            if(cb_thu.isSelected()){
                day+=cb_thu.getText()+"";
            }
            if(cb_thu.isSelected()){
                day+=cb_thu.getText()+"";
            }
            if(cb_fri.isSelected()){
                day+=cb_fri.getText()+"";
            }
            
            if(cb_sat.isSelected()){
                day+=cb_sat.getText()+"";
            }
            if(cb_sun.isSelected()){
                day+=cb_sun.getText()+"";
            }
            
            
        String time_day= cmb_tpd.getSelectedItem().toString();
        String times= cmb_timeslot.getSelectedItem().toString();
        
        
        try {
            pst=con.prepareStatement("insert into work_duration(no_working_day,days,time_pday,time_slot)values(?,?,?,?)");
           
            pst.setString(1, noDays);
            pst.setString(2, day);
            pst.setString(3, time_day);
            pst.setString(4, times);
          
           int k = pst.executeUpdate();
           
           if(k==1){
               JOptionPane.showMessageDialog(this," Added");
             
                cmb_nd.setSelectedItem("");
                cb_mon.setText("");
                cb_tue.setText("");
                cb_wed.setText("");
                cb_thu.setText("");
                cb_fri.setText("");
                cb_sat.setText("");
                cmb_tpd.setSelectedItem("");
                cmb_timeslot.setSelectedItem("");
              
                cmb_nd.requestFocus();
              
              
               
           }
           else{
               JOptionPane.showMessageDialog(this,"Error");
           }
            
        } catch (SQLException ex) {
            Logger.getLogger(ck.class.getName()).log(Level.SEVERE, null, ex);
        }   
        
    }//GEN-LAST:event_btn_saveActionPerformed

